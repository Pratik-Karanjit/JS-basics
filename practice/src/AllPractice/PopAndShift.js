//Pop removes the last element from the array and returns it
let myArray = ["a", "b", "c", "d", "e", "f"];

console.log(myArray.pop());
console.log(myArray);

//Shift removes the first element from the array and returns it
let mySpace = ["m", "n", "o", "p"];
console.log(mySpace.shift());
console.log(mySpace);
