// The push() method adds new items to the end of an array.

// The push() method changes the length of the array.

// The push() method returns the new length.

const fruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits.push("Kiwi")); //This returns length of the pushed array
console.log(fruits);

//Unshift adds new element to the beginning of the array and returns the length

const fruits_ = ["Banana", "Orange", "Apple", "Mango"];
console.log(fruits_.unshift("Lemon", "Pineapple"));
console.log(fruits_);
